#import "UIColor+ShakdColors.h"

@implementation UIColor (ShakdColors)

+ (UIColor *)charcoalColor {
    return [UIColor colorWithRed:59.0/255
                           green:59.0/255
                            blue:59.0/255
                           alpha:1.0];
}

+ (UIColor *)lighterGrayColor {
    return [UIColor colorWithRed:200.0/255
                           green:200.0/255
                            blue:200.0/255
                           alpha:1.0];
}

+ (UIColor *)salmonColor {
    return [UIColor colorWithRed:255.0/255
                           green:83.0/255
                            blue:74.0/255
                           alpha:1.0];
}


+ (UIColor *)turquoiseColor {
    return [UIColor colorWithRed:0.0/255
                           green:199.0/255
                            blue:120.0/255
                           alpha:1.0];
}
@end
